function gachaPickRarity() {
  const total = Object.values(GACHA_RARITY).reduce((s, r) => s + r.weight, 0);
  let rand = Math.random() * total;
  for (const [key, r] of Object.entries(GACHA_RARITY)) {
    rand -= r.weight;
    if (rand <= 0) return key;
  }
  return 'N';
}

function gachaPickTitle(rarity) {
  const pool = GACHA_POOL[rarity];
  const a = pool.A[Math.floor(Math.random() * pool.A.length)];
  const b = pool.B[Math.floor(Math.random() * pool.B.length)];
  return { rarity, title: `${a} ${b}`, a, b };
}

function gachaRoll() {
  const rarity = gachaPickRarity();
  return gachaPickTitle(rarity);
}

