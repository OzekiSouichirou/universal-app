function getInventory() {
  try { return JSON.parse(localStorage.getItem('gacha_inventory') || '[]'); }
  catch { return []; }
}

function saveInventory(inv) {
  localStorage.setItem('gacha_inventory', JSON.stringify(inv));
}

// 称号を追加。かぶりの場合はduplicateフラグをtrueで返す
function addToInventory(result) {
  const inv = getInventory();
  const existing = inv.find(i => i.title === result.title);
  let isDuplicate = false;
  if (existing) {
    existing.count = (existing.count || 1) + 1;
    isDuplicate = true;
  } else {
    inv.push({ ...result, count: 1 });
  }
  saveInventory(inv);
  return isDuplicate;
}
